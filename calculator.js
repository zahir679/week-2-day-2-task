const sum = (a, b) => a + b;

const subtract = (a, b) => a - b;

const multiply = (a, b) => a * b;

const divide = (a, b) => a / b;

const modulus = (a, b) => a % b;

const even = (num) => num % 2 == 0;

const odd = (num) => num % 2 == 1;

module.exports = { sum, subtract, multiply, divide, modulus, even, odd };
